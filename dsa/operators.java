                                           // assignment operator in java


 
public class operators {
 public static void main(String[] args) {
  int a =10;
 a += 5;  // a = a + 5
 System.out.println(a);  // 15
                                
 int b = 20;
 b -= 3;  // b = b - 3
 System.out.println(b);  // 17
                                
 int c = 12;
 c *= 2;  // c = c * 2
System.out.println(c);  // 24
                                
 int d = 24;
 d /= 4;  // d = d / 4
 System.out.println(d);  // 6
                                
 int e = 15;
 e %= 5;  // e = e % 5
 System.out.println(e);  // 0
 }
 }                